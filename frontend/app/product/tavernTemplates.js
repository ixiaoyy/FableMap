const PACKAGE_TYPE = 'fablemap_tavern_package'
const PACKAGE_VERSION = '1.0'

function makePackage({
  id,
  tavern,
  characters,
  worldInfo,
  tags,
  promptPreset,
}) {
  return {
    type: PACKAGE_TYPE,
    version: PACKAGE_VERSION,
    exported_at: '2026-04-17T00:00:00Z',
    source: {
      tavern_id: id,
      author_id: 'FableMap',
    },
    tavern: {
      id,
      name: tavern.name,
      description: tavern.description,
      lat: 31.2304,
      lon: 121.4737,
      address: '',
      access: 'private',
      status: 'closed',
      scene_prompt: tavern.scene_prompt,
      llm_config: promptPreset?.llm_config || {
        backend: 'openai',
        model: 'gpt-4o-mini',
        temperature: 0.85,
        max_tokens: 2048,
      },
    },
    characters,
    world_info: worldInfo,
    groups: [],
    bookmarks: [
      {
        id: `${id}_bookmark_style`,
        content: `模板标签：${tags.join(' / ')}`,
      },
    ],
    chat_templates: [],
    prompt_preset: promptPreset || {},
    memory_policy: {
      mode: 'visitor_state_mvp',
      note: '使用 FableMap 默认访客关系与记忆面板。',
    },
    voice_config: {
      enabled: false,
    },
    cover: '',
  }
}

